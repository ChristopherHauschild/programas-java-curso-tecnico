    private void jButtonCalcularCustoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularCustoActionPerformed
        // TODO add your handling code here:
        float kwh = (Float.parseFloat(jTextFieldKwh.getText()));

        String letraInicial = jComboBoxConsumidor.getSelectedItem().toString().substring(0,1);
        
        if (letraInicial.equals("R")){
            valor = kwh*0.3;
            resultado = Double.toString(valor);
            auxRes++;
            soma = soma+kwh;
            mediaR = Double.toString(soma/auxRes);
        }
        
        else if (letraInicial.equals("C")){
            valor = kwh*0.4;
            resultado = Double.toString(valor);
            auxCom++;
            soma = soma+kwh;
            mediaC = Double.toString(soma/auxCom);
        }
       
        else if (letraInicial.equals("I")){
            valor = kwh*0.7;
            resultado = Double.toString(valor);
            auxInd++;
            soma = soma+kwh;
            mediaI = Double.toString(soma/auxInd);
        }
        
        jLabelResultado.setText(resultado);
        jLabelRes.setText(mediaR);
        jLabelCom.setText(mediaC);
        jLabelInd.setText(mediaI);
        jTextFieldKwh.setText("");
        
    }//GEN-LAST:event_jButtonCalcularCustoActionPerformed

